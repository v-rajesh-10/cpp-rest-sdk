#include "url.h"

std::string urlEncode(const std::string& source) {
  std::string destination;
  for (std::size_t i = 0; i < source.size(); ++i) {
    char c = source[i];
    if (c == '*' || c == '-' || c == '.' || c >= '0' && c <= '9' || c >= 'A' && c <= 'Z' || c == '_' || c >= 'a' && c <= 'z') {
      destination += c;
    } else if (c == ' ') {
      destination += '+';
    } else {
      destination += '%';
      destination += "0123456789ABCDEF"[uint8_t(c) >> 4];
      destination += "0123456789ABCDEF"[c & 15];
    }
  }

  return destination;
}

